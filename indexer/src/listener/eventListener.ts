async function handleRoomCreated(event: RoomCreatedEvent): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'RoomCreated' });
  try {
    const roomPubkey = event.room.toBase58();
    const tokenMint = event.tokenMint.toBase58();
    const expiry = new Date(event.expiryTimestamp.toNumber() * 1000);

    const meta = await fetchTokenMeta(tokenMint);
    const openingPrice = BigInt(event.openingPrice.toString());

    const statusVal = 'active';

    await prisma.room.upsert({
      where: { roomPubkey },
      create: {
        roomPubkey,
        tokenMint,
        priceFeed: event.priceFeed.toBase58(),
        tokenName: meta.name ?? event.tokenName,
        tokenSymbol: meta.symbol,
        tokenImageUrl: meta.imageUrl,
        chainId: meta.chainId ?? 'solana',
        originalAddress: meta.originalAddress ?? tokenMint,
        duration: event.durationMinutes,
        openingPrice,
        expiry,
        status: statusVal,
        creator: event.creator.toBase58(),
      },
      update: {
        priceFeed: event.priceFeed.toBase58(),
        creator: event.creator.toBase58(),
      },
    });

    await cacheRoom(roomPubkey, {
      status: statusVal,
      tokenMint,
      tokenName: meta.name ?? event.tokenName,
      tokenSymbol: meta.symbol ?? '',
      tokenImageUrl: meta.imageUrl ?? '',
      openingPrice: openingPrice.toString(),
      moonPool: '0',
      jeetPool: '0',
      expiry: expiry.toISOString(),
      pairAddress: meta.pairAddress ?? '',
    });

    await publishRoomUpdate(roomPubkey, {
      type: 'RoomCreated',
      tokenName: meta.name ?? event.tokenName,
      tokenSymbol: meta.symbol,
      tokenImageUrl: meta.imageUrl,
      chainId: meta.chainId ?? 'solana',
      originalAddress: meta.originalAddress ?? tokenMint,
      expiry: expiry.toISOString(),
    });

    roomsCreatedTotal.inc();

    // Update active room gauge
    const activeCount = await prisma.room.count({ where: { status: 'active' } });
    activeRoomsGauge.set(activeCount);

    logger.info({ msg: 'RoomCreated processed', roomPubkey, tokenMint, openingPrice: openingPrice.toString() });
  } finally {
    end();
  }
}

async function handleBetPlaced(event: BetPlacedEvent, signature?: string): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'BetPlaced' });
  try {
    const roomPubkey = event.room.toBase58();
    const userPubkey = event.user.toBase58();
    const side = event.side === 0 ? 'moon' : 'jeet';
    const amount = BigInt(event.amount.toString());

    // Upsert bet — same user+room+side increments amount
    const existing = await prisma.bet.findFirst({
      where: { roomPubkey, userPubkey, side },
    });

    if (existing) {
      await prisma.bet.update({
        where: { id: existing.id },
        data: { amount: existing.amount + amount, txSig: signature || existing.txSig },
      });
    } else {
      await prisma.bet.create({
        data: { roomPubkey, userPubkey, side, amount, txSig: signature },
      });
    }

    // Update room pool totals in DB
    await prisma.room.update({
      where: { roomPubkey },
      data: {
        totalPool: {
          increment: amount,
        },
      },
    });

    // Update Redis cache
    const moonPool = event.moonPool.toString();
    const jeetPool = event.jeetPool.toString();
    await cacheRoom(roomPubkey, { moonPool, jeetPool });

    await publishRoomUpdate(roomPubkey, {
      type: 'BetPlaced',
      user: userPubkey,
      side,
      amount: amount.toString(),
      moonPool,
      jeetPool,
    });

    // Upsert user profile
    await prisma.userProfile.upsert({
      where: { userPubkey },
      create: { userPubkey, totalBets: 1 },
      update: { totalBets: { increment: 1 } },
    });

    betsPlacedTotal.inc({ side });
    betsVolumeTotal.inc(Number(amount));

    logger.info({ msg: 'BetPlaced processed', roomPubkey, userPubkey, side, amount: amount.toString() });
  } finally {
    end();
  }
}



async function handleRoomSettled(event: RoomSettledEvent): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'RoomSettled' });
  try {
    const roomPubkey = event.room.toBase58();

    const isDraw = event.winner === 2;
    const winner = isDraw ? 'draw' : (event.winner === 0 ? 'moon' : 'jeet');
    const finalPrice = BigInt(event.finalPrice.toString());
    const twapFinalPrice = BigInt(event.twapFinalPrice.toString());

    logger.info({
      msg: 'Processing RoomSettled event with direct on-chain values',
      roomPubkey,
      winner,
      finalPrice: finalPrice.toString(),
      twapFinalPrice: twapFinalPrice.toString()
    });

    const totalPool = BigInt(event.totalPool.toString());
    const platformFee = BigInt(event.platformFee.toString());
    const payoutPool = totalPool - platformFee;

    // Fetch winning bets: in a draw, all bets are "winning" bets to receive refunds proportionally
    const winningBets = isDraw 
      ? await prisma.bet.findMany({ where: { roomPubkey } })
      : await prisma.bet.findMany({ where: { roomPubkey, side: winner } });

    const winningPool = isDraw ? totalPool : winningBets.reduce((sum, b) => sum + b.amount, BigInt(0));

    // Compute and store individual payouts
    for (const bet of winningBets) {
      if (winningPool === BigInt(0)) continue;
      const payout = (bet.amount * payoutPool) / winningPool;

      const existingPayout = await prisma.payout.findFirst({
        where: { roomPubkey, userPubkey: bet.userPubkey },
      });
      if (existingPayout) {
        await prisma.payout.update({
          where: { id: existingPayout.id },
          data: { amount: payout },
        });
      } else {
        await prisma.payout.create({
          data: {
            roomPubkey,
            userPubkey: bet.userPubkey,
            amount: payout,
          },
        });
      }
    }

    // Update losers' stats (none in a Draw!)
    const losingBets = isDraw 
      ? [] 
      : await prisma.bet.findMany({ where: { roomPubkey, side: winner === 'moon' ? 'jeet' : 'moon' } });

    for (const bet of losingBets) {
      await prisma.userProfile.upsert({
        where: { userPubkey: bet.userPubkey },
        create: {
          userPubkey: bet.userPubkey,
          losses: 1,
          profit: -bet.amount,
        },
        update: {
          losses: { increment: 1 },
          profit: { decrement: bet.amount },
        },
      });
      await updateLeaderboard(bet.userPubkey, -bet.amount);
    }

    // Mark room settled in DB (including twapFinalPrice)
    await prisma.room.update({
      where: { roomPubkey },
      data: {
        status: 'settled',
        winner,
        finalPrice,
        twapFinalPrice,
        platformFee,
      },
    });

    // Update Redis cache
    await cacheRoom(roomPubkey, {
      status: 'settled',
      winner,
      finalPrice: finalPrice.toString(),
      twapFinalPrice: twapFinalPrice.toString(),
    });

    await publishRoomUpdate(roomPubkey, {
      type: 'RoomSettled',
      winner,
      finalPrice: finalPrice.toString(),
      twapFinalPrice: twapFinalPrice.toString(),
      totalPool: totalPool.toString(),
      platformFee: platformFee.toString(),
    });

    roomsSettledTotal.inc({ winner });

    const activeCount = await prisma.room.count({ where: { status: 'active' } });
    activeRoomsGauge.set(activeCount);

    logger.info({ msg: 'RoomSettled processed', roomPubkey, winner, finalPrice: finalPrice.toString(), twapFinalPrice: twapFinalPrice.toString() });

    // ─── Automatic Winner Payout Distribution ───
    if (winningBets.length > 0 && activeConnection) {
      logger.info({ msg: 'Triggering automatic on-chain payouts for winners', roomPubkey, winnersCount: winningBets.length });
      try {
        const keeper = getKeeperKeypair();
        const program = getProgram(activeConnection);
        const instructions: anchor.web3.TransactionInstruction[] = [];
        
        for (const bet of winningBets) {
          const winnerPubkey = new PublicKey(bet.userPubkey);
          const [escrowPda] = PublicKey.findProgramAddressSync(
            [Buffer.from('escrow'), event.room.toBuffer()],
            program.programId
          );
          const sideByte = bet.side === 'moon' ? 0 : 1;
          const [betPda] = PublicKey.findProgramAddressSync(
            [Buffer.from('bet'), event.room.toBuffer(), winnerPubkey.toBuffer(), Buffer.from([sideByte])],
            program.programId
          );
          
          const ix = await program.methods
            .claimWinnings()
            .accounts({
              room: event.room,
              escrow: escrowPda,
              bet: betPda,
              user: winnerPubkey,
              payer: keeper.publicKey,
              systemProgram: anchor.web3.SystemProgram.programId,
            })
            .instruction();
            
          instructions.push(ix);
        }
        
        // Batch instructions into transactions of at most 5 claims to avoid transaction size limits
        const BATCH_SIZE = 5;
        for (let i = 0; i < instructions.length; i += BATCH_SIZE) {
          const batch = instructions.slice(i, i + BATCH_SIZE);
          const tx = new anchor.web3.Transaction();
          
          // Add Compute Budget limit & Priority Fee instructions to guarantee inclusion in congested blocks
          const modifyComputeBudget = anchor.web3.ComputeBudgetProgram.setComputeUnitLimit({ 
            units: 150000 * batch.length // 150k compute budget per instruction is extremely safe
          });
          const addPriorityFee = anchor.web3.ComputeBudgetProgram.setComputeUnitPrice({ 
            microLamports: 50000 
          });
          
          tx.add(modifyComputeBudget);
          tx.add(addPriorityFee);
          tx.add(...batch);
          
          const txSig = await anchor.web3.sendAndConfirmTransaction(
            activeConnection,
            tx,
            [keeper],
            { commitment: 'confirmed', skipPreflight: true }
          );
          logger.info({
            msg: 'Automatic payout batch confirmed on-chain',
            roomPubkey,
            txSig,
            winnersCount: batch.length,
          });
        }
      } catch (payoutErr: any) {
        logger.error({
          msg: 'Automatic payout distribution failed',
          roomPubkey,
          err: payoutErr?.message || String(payoutErr),
        });
      }
    }

    // ─── Automatic Referral Payouts (0.1% reward) ───
    if (activeConnection) {
      try {
        const keeper = getKeeperKeypair();
        const roomBets = await prisma.bet.findMany({
          where: { roomPubkey },
        });

        const referralIxs: anchor.web3.TransactionInstruction[] = [];
        const referralPayoutData: { referrer: string; invitee: string; betAmount: bigint; rewardAmount: bigint }[] = [];

        for (const bet of roomBets) {
          const bettorProfile = await prisma.userProfile.findUnique({
            where: { userPubkey: bet.userPubkey },
            select: { referredBy: true }
          });

          if (bettorProfile && bettorProfile.referredBy) {
            const referrerWallet = new PublicKey(bettorProfile.referredBy);
            
            // Referrer gets 0.1% of bet amount (1/1000 of lamports)
            const betAmount = BigInt(bet.amount);
            const rewardAmount = betAmount / BigInt(1000);

            if (rewardAmount > BigInt(0)) {
              const ix = anchor.web3.SystemProgram.transfer({
                fromPubkey: keeper.publicKey,
                toPubkey: referrerWallet,
                lamports: Number(rewardAmount),
              });
              referralIxs.push(ix);
              referralPayoutData.push({
                referrer: bettorProfile.referredBy,
                invitee: bet.userPubkey,
                betAmount,
                rewardAmount
              });
            }
          }
        }

        if (referralIxs.length > 0) {
          logger.info({ msg: 'Triggering on-chain referral payouts', count: referralIxs.length, roomPubkey });
          
          const REF_BATCH_SIZE = 10;
          for (let i = 0; i < referralIxs.length; i += REF_BATCH_SIZE) {
            const batchIxs = referralIxs.slice(i, i + REF_BATCH_SIZE);
            const batchData = referralPayoutData.slice(i, i + REF_BATCH_SIZE);

            const tx = new anchor.web3.Transaction();
            tx.add(...batchIxs);

            const txSig = await anchor.web3.sendAndConfirmTransaction(
              activeConnection,
              tx,
              [keeper],
              { commitment: 'confirmed', skipPreflight: true }
            );

            logger.info({ msg: 'Referral payout batch confirmed on-chain', txSig, count: batchIxs.length });

            for (const item of batchData) {
              await prisma.referralPayout.create({
                data: {
                  referrer: item.referrer,
                  invitee: item.invitee,
                  roomPubkey,
                  betAmount: item.betAmount,
                  rewardAmount: item.rewardAmount,
                  txSig,
                }
              });
            }
          }
        }
      } catch (refErr: any) {
        logger.error({ msg: 'Referral payout failed', roomPubkey, err: refErr?.message || String(refErr) });
      }
    }
  } finally {
    end();
  }
}

async function handleWinningsClaimed(event: WingsClaimedEvent): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'WinningsClaimed' });
  try {
    const roomPubkey = event.room.toBase58();
    const userPubkey = event.user.toBase58();
    const amount = BigInt(event.amount.toString());

    // Mark bet as claimed
    await prisma.bet.updateMany({
      where: { roomPubkey, userPubkey },
      data: { claimed: true },
    });

    // Mark payout as claimed
    await prisma.payout.updateMany({
      where: { roomPubkey, userPubkey },
      data: { claimedAt: new Date() },
    });

    // Update user profile: wins + profit
    // Sum ALL bets for this user in this room (supports multi-bet per user)
    const userBets = await prisma.bet.findMany({
      where: { roomPubkey, userPubkey },
    });
    const totalBetAmount = userBets.reduce((sum, b) => sum + b.amount, BigInt(0));
    const profitGain = totalBetAmount > BigInt(0) ? amount - totalBetAmount : amount;

    await prisma.userProfile.upsert({
      where: { userPubkey },
      create: {
        userPubkey,
        wins: 1,
        profit: profitGain,
      },
      update: {
        wins: { increment: 1 },
        profit: { increment: profitGain },
      },
    });

    await updateLeaderboard(userPubkey, profitGain);

    await publishRoomUpdate(roomPubkey, {
      type: 'WinningsClaimed',
      user: userPubkey,
      amount: amount.toString(),
    });

    logger.info({ msg: 'WinningsClaimed processed', roomPubkey, userPubkey, amount: amount.toString() });
  } finally {
    end();
  }
}

