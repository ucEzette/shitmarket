async function handleDisputeResolved(event: DisputeResolvedEvent): Promise<void> {
  try {
    const roomPubkey = event.room.toBase58();
    const isDraw = event.winner === 2;
    const winner = isDraw ? 'draw' : (event.winner === 0 ? 'moon' : 'jeet');
    const overturned = event.refundChallenger;

    const room = await prisma.room.findUnique({
      where: { roomPubkey },
      include: { bets: true },
    });

    if (!room) return;

    const totalPool = room.totalPool;
    const platformFee = room.platformFee;
    const oracleFee = room.oracleFeeLamports;
    const payoutPool = totalPool - platformFee - oracleFee;

    // Clear existing payouts so we can write the new resolved payouts
    await prisma.payout.deleteMany({ where: { roomPubkey } });

    const winningBets = isDraw 
      ? room.bets 
      : room.bets.filter(b => b.side === winner);

    const winningPool = isDraw ? totalPool : winningBets.reduce((sum, b) => sum + b.amount, BigInt(0));

    const payoutOps = [];
    for (const bet of winningBets) {
      if (winningPool === BigInt(0)) continue;
      const payout = (bet.amount * payoutPool) / winningPool;

      payoutOps.push(
        prisma.payout.create({
          data: {
            roomPubkey,
            userPubkey: bet.userPubkey,
            amount: payout,
          },
        })
      );
    }

    const roomUpdate = prisma.room.update({
      where: { roomPubkey },
      data: {
        status: 'settled',
        winner,
        disputeStatus: overturned ? 1 : 0,
      },
    });

    await prisma.$transaction([
      roomUpdate,
      ...payoutOps
    ]);

    await cacheRoom(roomPubkey, {
      status: 'settled',
      winner,
    });

    await publishRoomUpdate(roomPubkey, {
      type: 'DisputeResolved',
      winner,
      overturned,
    });

    logger.info({ msg: 'DisputeResolved processed', roomPubkey, winner, overturned });
  } catch (err: any) {
    logger.error({ msg: 'DisputeResolved failed', err: err.message });
  }
}

let subscriptionId: number | null = null;

export async function processParsedEvents(events: any[], signature: string): Promise<void> {
  if (await isAlreadyProcessed(signature)) {
    logger.debug({ msg: 'Duplicate tx skipped', signature });
    return;
  }

  for (const event of events) {
    logger.debug({ msg: 'Processing event', name: event.name, signature });

    switch (event.name) {
      case 'RoomCreated':
        await handleRoomCreated(event.data as RoomCreatedEvent);
        break;

      case 'BetPlaced':
        await handleBetPlaced(event.data as BetPlacedEvent, signature);
        break;
      case 'RoomSettled':
        await handleRoomSettled(event.data as RoomSettledEvent);
        break;
      case 'WinningsClaimed':
        await handleWinningsClaimed(event.data as WingsClaimedEvent);
        break;
      case 'PlatformPaused':
        await handlePlatformPaused(event.data as PlatformPausedEvent);
        break;
      case 'ReputationUpdated':
        await handleReputationUpdated(event.data as ReputationUpdatedEvent);
        break;
      case 'RoomVoided':
        await handleRoomVoided(event.data as RoomVoidedEvent);
        break;
      case 'ReferralRegistered':
        await handleReferralRegistered(event.data as ReferralRegisteredEvent);
        break;
      case 'ReferralRewardAccrued':
        await handleReferralRewardAccrued(event.data as ReferralRewardAccruedEvent, signature);
        break;
      case 'ReferralRewardsClaimed':
        await handleReferralRewardsClaimed(event.data as ReferralRewardsClaimedEvent, signature);
        break;
      case 'PositionListed':
        await handlePositionListed(event.data as PositionListedEvent);
        break;
      case 'PositionPurchased':
        await handlePositionPurchased(event.data as PositionPurchasedEvent);
        break;
      case 'ListingCancelled':
        await handleListingCancelled(event.data as ListingCancelledEvent);
        break;
      case 'RoomDisputed':
        await handleRoomDisputed(event.data as RoomDisputedEvent);
        break;
      case 'DisputeResolved':
        await handleDisputeResolved(event.data as DisputeResolvedEvent);
        break;
      default:
        logger.warn({ msg: 'Unknown event', name: event.name });
    }
  }

  await markProcessed(signature);
}

export function startEventListener(
  connection: Connection,
  eventParser: anchor.EventParser
): void {
  activeConnection = connection;
  eventListenerStatus.isRunning = true;
  const programId = new PublicKey(config.solana.programId);

  subscriptionId = connection.onLogs(
    programId,
    async (logs: Logs) => {
      const { signature, logs: logLines, err } = logs;
      eventListenerStatus.lastEventReceivedAt = new Date().toISOString();
      eventListenerStatus.lastError = null;

      if (err) {
        eventListenerStatus.lastError = String(err);
        logger.warn({ msg: 'Log with error skipped', signature, err });
        return;
      }

      // Idempotency guard
      if (await isAlreadyProcessed(signature)) {
        logger.debug({ msg: 'Duplicate tx skipped', signature });
        eventListenerStatus.lastEventProcessedAt = new Date().toISOString();
        return;
      }

      // Parse Anchor events from log lines
      try {
        const events = Array.from(eventParser.parseLogs(logLines));
        if (events.length > 0) {
          await processParsedEvents(events, signature);
        }
        eventListenerStatus.lastEventProcessedAt = new Date().toISOString();
      } catch (err: any) {
        eventListenerStatus.lastError = err?.message ?? String(err);
        logger.error({ msg: 'Event processing error', signature, err: err?.message, stack: err?.stack });
      }
    },
    'confirmed'
  );

  logger.info({ msg: 'Event listener started', programId: programId.toBase58() });
}

export async function stopEventListener(connection: Connection): Promise<void> {
  if (subscriptionId !== null) {
    await connection.removeOnLogsListener(subscriptionId);
    subscriptionId = null;
    logger.info('Event listener stopped');
  }
}
