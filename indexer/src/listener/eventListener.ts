/**
 * Phase 3.5: Handle ReputationUpdated event.
 */
async function handleReputationUpdated(event: ReputationUpdatedEvent): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'ReputationUpdated' });
  try {
    const userPubkey = event.user.toBase58();
    const tierMap = ['D', 'C', 'B', 'A', 'S'];
    const tier = tierMap[event.tier] ?? 'D';
    const totalBets = BigInt(event.totalBets.toString());
    const totalWins = BigInt(event.totalWins.toString());

    // Upsert reputation record
    await prisma.reputation.upsert({
      where: { userPubkey },
      create: {
        userPubkey,
        tier,
        totalBets,
        totalWins,
      },
      update: {
        tier,
        totalBets,
        totalWins,
        lastEval: new Date(),
      },
    });

    // Also update the user profile with the tier
    await prisma.userProfile.upsert({
      where: { userPubkey },
      create: {
        userPubkey,
        trenchScore: tier,
      },
      update: {
        trenchScore: tier,
      },
    });

    logger.info({
      msg: 'ReputationUpdated processed',
      userPubkey,
      tier,
      totalBets: totalBets.toString(),
      totalWins: totalWins.toString(),
    });

    await publishRoomUpdate('__reputation__', {
      type: 'ReputationUpdated',
      user: userPubkey,
      tier,
    });
  } finally {
    end();
  }
}

async function handleRoomVoided(event: RoomVoidedEvent): Promise<void> {
  const end = eventProcessingDuration.startTimer({ event_type: 'RoomVoided' });
  try {
    const roomPubkey = event.room.toBase58();

    logger.info({
      msg: 'Processing RoomVoided event (one-sided void)',
      roomPubkey,
      reason: event.reason,
    });

    const totalPool = BigInt(event.totalRefundPool.toString());
    const platformFee = BigInt(0);

    // In a voided room, everyone wins (i.e. receives full refund)
    const winningBets = await prisma.bet.findMany({ where: { roomPubkey } });

    // Compute and store individual payouts (100% refund of bet amount) inside a transaction-ready format
    const payoutOps = [];
    for (const bet of winningBets) {
      const payout = bet.amount;
      payoutOps.push(
        prisma.payout.upsert({
          where: {
            roomPubkey_userPubkey: {
              roomPubkey,
              userPubkey: bet.userPubkey,
            },
          },
          create: {
            roomPubkey,
            userPubkey: bet.userPubkey,
            amount: payout,
          },
          update: {
            amount: payout,
          },
        })
      );
    }

    // Mark room settled in DB as draw (representing void refund path)
    const roomUpdateOp = prisma.room.update({
      where: { roomPubkey },
      data: {
        status: 'settled',
        winner: 'draw',
        platformFee,
      },
    });

    // Execute database updates inside a transaction
    await prisma.$transaction([
      ...payoutOps,
      roomUpdateOp,
    ]);

    // Update Redis cache
    await cacheRoom(roomPubkey, {
      status: 'settled',
      winner: 'draw',
    });

    await publishRoomUpdate(roomPubkey, {
      type: 'RoomSettled',
      winner: 'draw',
      totalPool: totalPool.toString(),
      platformFee: platformFee.toString(),
    });

    roomsSettledTotal.inc({ winner: 'draw' });

    const activeCount = await prisma.room.count({ where: { status: 'active' } });
    activeRoomsGauge.set(activeCount);

    logger.info({ msg: 'RoomVoided processed successfully', roomPubkey });

    // ═══════════════════════════════════════════════════════════════
    // Automatic void refund payouts REMOVED — users must manually
    // claim their void refunds via the frontend "Claim" button.
    //
    // Reasoning is the same as for RoomSettled auto-payouts:
    // prevents escrow drain from race conditions and avoids
    // referral account issues when the keeper claims on behalf
    // of users.
    // ═══════════════════════════════════════════════════════════════
  } finally {
    end();
  }
}

