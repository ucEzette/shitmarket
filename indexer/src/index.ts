/**
 * index.ts — Main entry point.
 *
 * Boots services in order:
 * 1. DB (Prisma migrations)
 * 2. Redis (command + subscriber connections)
 * 3. Solana RPC circuit breaker
 * 4. REST API server
 * 5. WebSocket server
 * 6. Solana event listener
 * 7. Settlement keeper
 *
 * Graceful shutdown on SIGTERM/SIGINT.
 */

