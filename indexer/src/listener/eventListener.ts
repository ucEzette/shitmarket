let keeperKeypair: Keypair | null = null;

function getKeeperKeypair(): Keypair {
  if (keeperKeypair) return keeperKeypair;
  const raw = config.solana.keeperPrivateKey;
  try {
    if (raw.startsWith('[')) {
      const arr: number[] = JSON.parse(raw);
      keeperKeypair = Keypair.fromSecretKey(Uint8Array.from(arr));
    } else {
      keeperKeypair = Keypair.fromSecretKey(bs58.decode(raw));
    }
    return keeperKeypair;
  } catch (err) {
    throw new Error(`Invalid KEEPER_PRIVATE_KEY format in eventListener: ${err}`);
  }
}

function getProgram(connection: Connection): anchor.Program {
  if (activeProgram) return activeProgram;
  
  const programId = new PublicKey(config.solana.programId);
  let idl: any;
  try {
    idl = require('../../../program/target/idl/shitmarket.json');
  } catch {
    try {
      idl = require('../utils/idl.json');
    } catch {
      try {
        idl = require('../utils/idl.json');
      } catch {
        throw new Error('Failed to load IDL in eventListener');
      }
    }
  }

  const wallet = new anchor.Wallet(anchor.web3.Keypair.generate());
  const provider = new anchor.AnchorProvider(connection, wallet, {
    commitment: 'confirmed',
  });
  
  const idlWithAddress = { ...idl, address: programId.toBase58() };
  
  function normalizeIdl(obj: any): void {
    if (Array.isArray(obj)) {
      obj.forEach(normalizeIdl);
    } else if (obj !== null && typeof obj === 'object') {
      for (const key in obj) {
        if (key === 'defined' && typeof obj[key] === 'string') {
          obj[key] = { name: obj[key] };
        } else {
          normalizeIdl(obj[key]);
        }
      }
    }
  }
  normalizeIdl(idlWithAddress);
  
  activeProgram = new anchor.Program(idlWithAddress as anchor.Idl, provider);
  return activeProgram;
}
