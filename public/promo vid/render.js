const { chromium } = require('playwright');
const path = require('path');
const fs = require('fs');
const { execSync } = require('child_process');

(async () => {
    console.log("🚀 Starting Playwright Renderer...");

    const tempDir = path.join(__dirname, 'temp_video');
    if (!fs.existsSync(tempDir)) fs.mkdirSync(tempDir);
    else fs.readdirSync(tempDir).forEach(file => fs.unlinkSync(path.join(tempDir, file)));

    const browser = await chromium.launch({
        headless: true
    });

    const context = await browser.newContext({
        viewport: { width: 1920, height: 1080 },
        recordVideo: {
            dir: tempDir,
            size: { width: 1920, height: 1080 }
        }
    });

    const page = await context.newPage();
    
    // We mock audio so it doesn't complain about AutoPlay policy or block headless recording
    await page.addInitScript(() => {
        window.AudioContext = class {
            createOscillator() { return { connect: () => {}, frequency: { setValueAtTime: () => {}, exponentialRampToValueAtTime: () => {} }, start: () => {}, stop: () => {}, type: '' }; }
            createGain() { return { connect: () => {}, gain: { setValueAtTime: () => {}, exponentialRampToValueAtTime: () => {} } }; }
            createMediaStreamDestination() { return { stream: { getAudioTracks: () => [] } }; }
            get destination() { return {}; }
            get currentTime() { return 0; }
        };
        window.webkitAudioContext = window.AudioContext;
    });

    const filePath = `file://${path.join(__dirname, 'code.html')}`;
    console.log(`📄 Loading: ${filePath}`);
    await page.goto(filePath, { waitUntil: 'networkidle' });

    // Wait 1s for fonts/assets
    await page.waitForTimeout(1000);

    console.log("💥 Triggering sequence...");
    await page.click('#btn-deploy');

    // Wait exactly 6 seconds for animation to complete fully
    console.log("🎥 Recording for 6 seconds...");
    await page.waitForTimeout(6000);

    // Closing the context automatically stops the recording and flushes the file to disk
    await context.close();
    await browser.close();

    console.log("🎬 Formatting video...");
    const files = fs.readdirSync(tempDir).filter(f => f.endsWith('.webm'));
    if (files.length > 0) {
        const recordedVideo = path.join(tempDir, files[0]);
        const outputVideo = path.join(__dirname, 'shitmarket_promo.mp4');
        try {
            execSync(`ffmpeg -y -i "${recordedVideo}" -c:v libx264 -preset fast -crf 18 -pix_fmt yuv420p "${outputVideo}"`, { stdio: 'inherit' });
            console.log(`✨ SUCCESS! Final video: ${outputVideo}`);
        } catch (e) {
            console.error("FFmpeg failed", e.message);
        }
        
        fs.unlinkSync(recordedVideo);
        fs.rmdirSync(tempDir);
    } else {
        console.log("❌ No video file was generated by Playwright.");
    }
})();
